_MEDIAMTX_CONFIG_TEMPLATE = """\
# Generated by door-media — do not edit by hand.
# All ports bound to 127.0.0.1 to prevent off-host exposure (ARCHITECTURE §security 16).

logLevel: info
logDestinations: [stdout]

api: yes
apiAddress: 127.0.0.1:9997

# Disable all off-host protocols
rtspAddress: 127.0.0.1:{rtsp_port}
rtspsAddress: 127.0.0.1:8322
rtmpAddress: 127.0.0.1:1935
rtmpsAddress: 127.0.0.1:1936
hlsAddress: 127.0.0.1:8888

# WebRTC via WHEP/WHIP — local only
webrtcAddress: 127.0.0.1:8889

pathDefaults:
  record: no

paths:
  {stream}:
    source: publisher
    record: yes
    recordPath: {segments_root}/{stream}/%Y/%m/%d/%H/%M/%S-%f
    recordFormat: fmp4
    recordSegmentDuration: {segment_s}s
    # Pi 5 has no HW H.264 block: rpicam-vid encodes via libav, which needs an
    # explicit --libav-format when writing the elementary stream to stdout.
    # MediaMTX runOnInit is NOT run through a shell, so the pipe must be wrapped
    # in sh -c. ffmpeg is quieted (-nostats -loglevel error) so its progress
    # output cannot fill the (unread) subprocess stdout pipe. (issue #84)
    # The publish command is built in _build_run_on_init and is VIDEO-ONLY.
    # Audio does NOT go through MediaMTX: its live fmp4 recorder cannot mux an
    # AAC track — with a second (mic) track it drops the RTSP publish and writes
    # zero segments after ~10s (verified on hardware, superseding #109). Mic
    # audio is instead captured to a per-recording file and muxed into the
    # concatenated video at finalize (see start_recording/finalize_recording).
    runOnInit: >-
      {run_on_init}
    runOnInitRestart: yes
"""


def _build_run_on_init(settings: Settings) -> str:
    """Return the MediaMTX ``runOnInit`` publish command for the visitor stream.

    The command is a ``sh -c`` wrapper (MediaMTX does not run runOnInit through a
    shell) around ``rpicam-vid | ffmpeg``. Video is always copied (``-c:v copy``);
    the Pi 5 has no HW H.264 block so rpicam-vid encodes via libav with an
    explicit ``--libav-format h264`` and baseline/4.1 profile.

    This command is **video-only** regardless of ``audio_enabled``. Audio does
    not go through MediaMTX: its live fmp4 recorder cannot mux an AAC track (it
    drops the RTSP publish and stops writing segments ~10s in — verified on
    hardware). Mic audio is captured separately per recording and muxed into the
    concatenated video at finalize instead. Building the string here keeps the
    rpicam/ffmpeg pipeline out of the config template's ``str.format`` pass, so
    no ffmpeg option can collide with the
    ``{stream}``/``{segments_root}``/``{segment_s}`` placeholders.
    """
    stream = settings.visitor_cam_stream
    rpicam = (
        "rpicam-vid --width 1280 --height 720 --framerate 25 --codec h264 "
        "--libav-format h264 --profile baseline --level 4.1 --bitrate 2000000 "
        "--inline --flush 1 --timeout 0 --nopreview --output -"
    )
    rtsp = f"-f rtsp -rtsp_transport tcp {settings.mediamtx_rtsp_url(stream)}"
    ffmpeg = (
        f"ffmpeg -nostats -loglevel error -fflags nobuffer -f h264 -r 25 -i pipe:0 -c:v copy {rtsp}"
    )
    return f"sh -c '{rpicam} | {ffmpeg}'"


@dataclass
class _ActiveRecording:
    handle: RecordingHandle
    started_monotonic: float = field(default_factory=time.monotonic)
    started_epoch: float = field(default_factory=time.time)
    # Segment files captured during this window (collected at finalize time)
    captured_segments: list[Path] = field(default_factory=list)
    # Parallel USB-mic capture (video-only MediaMTX path, muxed at finalize).
    # ``audio_proc`` runs ffmpeg writing ``audio_path`` (a per-recording .m4a);
    # ``audio_drain`` keeps its output pipe from filling. All None when audio is
    # disabled or the capture process failed to spawn (video-only fallback).
    audio_proc: asyncio.subprocess.Process | None = None
    audio_path: Path | None = None
    audio_drain: asyncio.Task[None] | None = None


class MediaMTXRouter:
    """Production MediaMTX-backed router.

    The subprocess management follows a simple supervisor loop:
      - Start MediaMTX.
      - If it exits unexpectedly, restart after a short back-off.
      - Stop cleanly on shutdown.
    """

    def __init__(self, settings: Settings) -> None:
        self._settings = settings
        self._proc: asyncio.subprocess.Process | None = None
        self._active: dict[UUID, _ActiveRecording] = {}
        self._http = httpx.AsyncClient(
            base_url=settings.mediamtx_api,
            timeout=httpx.Timeout(5.0),
        )
        self._supervisor_task: asyncio.Task[None] | None = None
        self._segment_cleanup_task: asyncio.Task[None] | None = None

    # ------------------------------------------------------------------
    # Lifecycle
    # ------------------------------------------------------------------

    async def start(self) -> None:
        """Write config and start the MediaMTX supervisor."""
        self._write_config()
        self._supervisor_task = asyncio.create_task(
            self._supervisor_loop(), name="mediamtx-supervisor"
        )
        self._segment_cleanup_task = asyncio.create_task(
            self._segment_cleanup_loop(), name="mediamtx-segment-cleanup"
        )

    async def stop(self) -> None:
        """Shut down MediaMTX and cancel the supervisor."""
        for task in (self._supervisor_task, self._segment_cleanup_task):
            if task:
                task.cancel()
                with contextlib.suppress(asyncio.CancelledError):
                    await task
        await self._kill_mediamtx()
        await self._http.aclose()

    async def _supervisor_loop(self) -> None:
        backoff = 1.0
        while True:
            try:
                logger.info("mediamtx_starting")
                self._proc = await asyncio.create_subprocess_exec(
                    self._settings.mediamtx_bin,
                    str(self._settings.mediamtx_config_path),
                    stdout=asyncio.subprocess.PIPE,
                    stderr=asyncio.subprocess.STDOUT,
                )
                # An unread stdout pipe fills its OS buffer and deadlocks
                # MediaMTX (API + streaming freeze while the process lives on).
                drain = asyncio.create_task(self._drain_output(self._proc.stdout))
                try:
                    await self._proc.wait()
                finally:
                    drain.cancel()
                    with contextlib.suppress(asyncio.CancelledError):
                        await drain
                rc = self._proc.returncode
                logger.warning(
                    "mediamtx_exited",
                    extra={"returncode": rc, "backoff_s": backoff},
                )
                await asyncio.sleep(backoff)
                backoff = min(backoff * 2, 30.0)
            except asyncio.CancelledError:
                break
            except Exception as exc:
                logger.exception("mediamtx_supervisor_error", exc_info=exc)
                await asyncio.sleep(backoff)
                backoff = min(backoff * 2, 30.0)

    async def _drain_output(
        self, stream: asyncio.StreamReader | None, *, label: str = "mediamtx"
    ) -> None:
        """Forward a subprocess's output to the logger so its pipe never fills."""
        if stream is None:
            return
        try:
            while True:
                line = await stream.readline()
                if not line:
                    break
                text = line.decode("utf-8", errors="replace").rstrip()
                if text:
                    logger.info(label, extra={"line": text})
        except asyncio.CancelledError:
            raise
        except Exception:
            logger.exception("%s_drain_failed", label)

    async def _kill_mediamtx(self) -> None:
        if self._proc and self._proc.returncode is None:
            try:
                self._proc.send_signal(signal.SIGTERM)
                try:
                    await asyncio.wait_for(self._proc.wait(), timeout=5.0)
                except TimeoutError:
                    self._proc.kill()
                    await self._proc.wait()
            except ProcessLookupError:
                pass

    async def _segment_cleanup_loop(self) -> None:
        while True:
            try:
                await asyncio.sleep(self._settings.segment_cleanup_interval_s)
                self.prune_segments()
            except asyncio.CancelledError:
                raise
            except Exception:
                logger.exception("segment_cleanup_failed")

    def prune_segments(self, *, now_epoch: float | None = None) -> int:
        """Delete expired raw segments without touching active windows."""
        now = time.time() if now_epoch is None else now_epoch
        cutoff = now - self._settings.segment_retention_s
        if self._active:
            earliest_active = min(item.started_epoch for item in self._active.values())
            cutoff = min(cutoff, earliest_active - self._settings.segment_s)
        deleted = _prune_segments(self._settings.segments_root, older_than_epoch=cutoff)
        if deleted:
            logger.info("segments_pruned", extra={"count": deleted, "cutoff_epoch": cutoff})
        return deleted

    def _write_config(self) -> None:
        cfg_path = self._settings.mediamtx_config_path
        cfg_path.parent.mkdir(parents=True, exist_ok=True)
        cfg_content = _MEDIAMTX_CONFIG_TEMPLATE.format(
            stream=self._settings.visitor_cam_stream,
            segments_root=self._settings.segments_root,
            segment_s=self._settings.segment_s,
            rtsp_port=self._settings.mediamtx_rtsp_port,
            run_on_init=_build_run_on_init(self._settings),
        )
        cfg_path.write_text(cfg_content, encoding="utf-8")
        logger.info("mediamtx_config_written", extra={"path": str(cfg_path)})

    # ------------------------------------------------------------------
    # MediaRouter protocol
    # ------------------------------------------------------------------

    async def start_recording(
        self,
        *,
        session_id: UUID,
        kind: RecordingKind,
        stream: str,
    ) -> RecordingHandle:
        """Start a recording window — MediaMTX is already writing segments."""
        recording_id = uuid7()
        handle = RecordingHandle(
            recording_id=recording_id,
            session_id=session_id,
            kind=kind,
            stream=stream,
            started_monotonic_ms=time.monotonic_ns() // 1_000_000,
        )
        active = _ActiveRecording(handle=handle)
        self._active[recording_id] = active
        if self._settings.audio_enabled:
            await self._start_audio_capture(active)
        logger.info(
            "recording_started",
            extra={
                "recording_id": str(recording_id),
                "kind": kind,
                "session_id": str(session_id),
                "audio": active.audio_proc is not None,
            },
        )
        return handle

    async def _start_audio_capture(self, active: _ActiveRecording) -> None:
        """Spawn a parallel ffmpeg capturing the USB mic to a per-recording file.

        The camera and mic are independent devices, so mic audio is recorded on
        its own (video stays MediaMTX-only) and muxed into the concatenated video
        at finalize. Failure to spawn is non-fatal: we log and fall back to a
        video-only recording rather than break capture.
        """
        recording_id = active.handle.recording_id
        audio_path = self._settings.audio_tmp_root / f"{recording_id}.m4a"
        try:
            audio_path.parent.mkdir(parents=True, exist_ok=True)
            # -nostdin: this ffmpeg shares the parent's stdin; without it ffmpeg
            # would consume/interfere with it. Output is piped and drained to
            # avoid the 64KB-pipe deadlock (same class of bug as the supervisor).
            proc = await asyncio.create_subprocess_exec(
                "ffmpeg",
                "-nostdin",
                "-nostats",
                "-loglevel",
                "error",
                "-f",
                "alsa",
                "-ar",
                str(self._settings.audio_sample_rate),
                "-i",
                self._settings.audio_device,
                "-c:a",
                "aac",
                "-b:a",
                self._settings.audio_bitrate,
                "-y",
                str(audio_path),
                stdin=asyncio.subprocess.DEVNULL,
                stdout=asyncio.subprocess.PIPE,
                stderr=asyncio.subprocess.STDOUT,
            )
        except Exception as exc:
            logger.warning(
                "audio_capture_start_failed",
                extra={"recording_id": str(recording_id), "error": str(exc)},
            )
            return
        active.audio_proc = proc
        active.audio_path = audio_path
        active.audio_drain = asyncio.create_task(
            self._drain_output(proc.stdout, label="audio_capture")
        )
        logger.info(
            "audio_capture_started",
            extra={"recording_id": str(recording_id), "path": str(audio_path)},
        )

    async def _stop_audio_capture(self, active: _ActiveRecording) -> None:
        """Gracefully stop the mic capture so ffmpeg finalizes the .m4a moov box.

        SIGTERM lets ffmpeg flush; kill only if it does not exit promptly. The
        output-drain task is always cancelled. Safe to call when no audio process
        was ever spawned.
        """
        proc = active.audio_proc
        if proc is not None and proc.returncode is None:
            with contextlib.suppress(ProcessLookupError):
                proc.terminate()
                try:
                    await asyncio.wait_for(proc.wait(), timeout=5.0)
                except TimeoutError:
                    with contextlib.suppress(ProcessLookupError):
                        proc.kill()
                    await proc.wait()
        if active.audio_drain is not None:
            active.audio_drain.cancel()
            with contextlib.suppress(asyncio.CancelledError):
                await active.audio_drain
            active.audio_drain = None

    async def finalize_recording(
        self,
        handle: RecordingHandle,
        *,
        consent_context: ConsentContext,
    ) -> FinalizedRecording:
        """Cut and remux the recording window into a single MP4."""
        active = self._active.get(handle.recording_id)
        if active is None:
            raise RuntimeError(f"recording {handle.recording_id} is not active")
        elapsed_s = time.monotonic() - active.started_monotonic

        out_path = self._settings.recordings_root / f"{handle.kind}_{handle.recording_id}.mp4"
        out_path.parent.mkdir(parents=True, exist_ok=True)

        # Find segments that overlap the recording window
        seg_dir = self._settings.segments_root / handle.stream
        ended_epoch = time.time()
        segments = _find_segments(
            seg_dir,
            started_epoch=active.started_epoch - self._settings.segment_s,
            ended_epoch=ended_epoch + self._settings.segment_s,
        )

        # The recording window is over: stop the parallel mic capture so ffmpeg
        # finalizes its .m4a. Always stop (even on the error paths below) so the
        # capture process is never leaked.
        await self._stop_audio_capture(active)

        if not segments:
            self._cleanup_audio_file(active)
            raise RuntimeError(f"no media segments found for recording {handle.recording_id}")
        # Concatenate the padded window of WHOLE 2s segments so the real
        # recording window is fully covered. out_path holds this video-only
        # concat; when audio is present it is trimmed to the real window and
        # remuxed below (otherwise it is the final video-only clip).
        await _concat_segments(segments, out_path)
        if not out_path.exists() or out_path.stat().st_size == 0:
            self._cleanup_audio_file(active)
            raise RuntimeError(f"ffmpeg produced an empty recording {handle.recording_id}")

        # The concat is padded by segment_s on each side AND snaps to 2s segment
        # boundaries, so it includes pre-roll before the visitor actually started
        # recording. The parallel mic audio covers the EXACT window
        # [started_epoch, ended_epoch]. Compute how far into the concat the real
        # start is (trim_start) and the real duration (D) so the audio mux can
        # trim the video to match — dropping the pre-roll and fixing A/V sync.
        trim_start = self._trim_start_into_concat(active.started_epoch, segments[0])
        duration_s = max(0.0, ended_epoch - active.started_epoch)

        # Mux the captured mic audio into the concatenated video, trimming the
        # video to the real window. Graceful video-only fallback on any problem —
        # audio/trim must never break a recording.
        await self._mux_audio(active, out_path, trim_start=trim_start, duration_s=duration_s)
        self._active.pop(handle.recording_id, None)

        size_bytes = out_path.stat().st_size
        sha256 = _sha256_file(out_path)
        rel_path = str(out_path.relative_to(self._settings.ssd_data_root))

        logger.info(
            "recording_finalized",
            extra={
                "recording_id": str(handle.recording_id),
                "path": rel_path,
                "duration_s": elapsed_s,
                "size_bytes": size_bytes,
                "sha256": sha256,
            },
        )
        return FinalizedRecording(
            recording_id=handle.recording_id,
            path=rel_path,
            duration_s=elapsed_s,
            size_bytes=size_bytes,
            sha256=sha256,
            consent_context=consent_context,
        )

    async def discard_recording(self, handle: RecordingHandle) -> None:
        """Abort an active recording window without cutting a clip."""
        active = self._active.pop(handle.recording_id, None)
        if active is not None:
            await self._stop_audio_capture(active)
            self._cleanup_audio_file(active)
        logger.info(
            "recording_discarded",
            extra={"recording_id": str(handle.recording_id)},
        )

    def _trim_start_into_concat(self, started_epoch: float, first_segment: Path) -> float | None:
        """How far into the concatenated video the real recording start lies.

        The concat begins at the first segment's start (which precedes the real
        ``started_epoch`` because the window is padded by ``segment_s`` and snaps
        to 2s segment boundaries). ``trim_start`` = ``started_epoch`` minus that
        segment start, clamped to >= 0. Prefer the start time encoded in the
        MediaMTX segment path (``%S-%f``); fall back to the file mtime minus one
        segment length if the path cannot be parsed. Returns ``None`` if neither
        is available (caller then keeps the untrimmed video-only concat).
        """
        try:
            seg_start = _segment_start_epoch(first_segment)
        except (ValueError, OSError, IndexError):
            try:
                seg_start = first_segment.stat().st_mtime - self._settings.segment_s
            except OSError:
                return None
        return max(0.0, started_epoch - seg_start)

    async def _mux_audio(
        self,
        active: _ActiveRecording,
        out_path: Path,
        *,
        trim_start: float | None,
        duration_s: float,
    ) -> None:
        """Trim the concat to the real window and mux the mic audio in place.

        ``out_path`` holds the padded video-only concat. A single ffmpeg pass
        input-seeks to ``trim_start`` and re-encodes (frame-accurate) for
        ``duration_s`` while muxing the captured audio, so the final clip matches
        the audio window: pre-roll dropped, A/V in sync.

        No-op (video-only) when audio was disabled, never captured, or the file
        is missing/empty. Graceful fallback (keep the untrimmed video-only
        ``out_path``) when the trim window can't be computed or ffmpeg fails —
        audio/trim must never break a recording. The per-recording temp audio
        file is always removed afterwards.
        """
        audio_path = active.audio_path
        if audio_path is None:
            return
        recording_id = active.handle.recording_id
        try:
            if not audio_path.exists() or audio_path.stat().st_size == 0:
                logger.warning(
                    "audio_missing_video_only",
                    extra={"recording_id": str(recording_id)},
                )
                return
            if trim_start is None or duration_s <= 0:
                logger.warning(
                    "audio_trim_unavailable_video_only",
                    extra={
                        "recording_id": str(recording_id),
                        "trim_start": trim_start,
                        "duration_s": duration_s,
                    },
                )
                return
            muxed_tmp = out_path.parent / f".muxed_{out_path.name}"
            offset = self._settings.audio_sync_offset_s
            # Input -ss before -i + a re-encode = frame-accurate trim: drops the
            # pre-roll ahead of the real start. -itsoffset shifts the *audio*
            # (next input); positive delays audio for lip-sync tuning
            # (MEDIA_AUDIO_SYNC_OFFSET_S). -t caps the output at the real window
            # so the trailing pad is dropped and video length matches audio.
            proc = await asyncio.create_subprocess_exec(
                "ffmpeg",
                "-nostdin",
                "-y",
                "-ss",
                f"{trim_start:.6f}",
                "-i",
                str(out_path),
                "-itsoffset",
                str(offset),
                "-i",
                str(audio_path),
                "-t",
                f"{duration_s:.6f}",
                "-c:v",
                "libx264",
                "-preset",
                "veryfast",
                "-crf",
                "23",
                "-pix_fmt",
                "yuv420p",
                "-c:a",
                "copy",
                "-map",
                "0:v:0",
                "-map",
                "1:a:0",
                str(muxed_tmp),
                stdin=asyncio.subprocess.DEVNULL,
                stdout=asyncio.subprocess.DEVNULL,
                stderr=asyncio.subprocess.PIPE,
            )
            _, stderr = await proc.communicate()
            if proc.returncode != 0 or not muxed_tmp.exists() or muxed_tmp.stat().st_size == 0:
                message = stderr.decode(errors="replace")[:500]
                logger.warning(
                    "audio_mux_failed_video_only",
                    extra={
                        "recording_id": str(recording_id),
                        "returncode": proc.returncode,
                        "error": message,
                    },
                )
                with contextlib.suppress(OSError):
                    muxed_tmp.unlink(missing_ok=True)
                return
            muxed_tmp.replace(out_path)
            logger.info(
                "audio_muxed",
                extra={
                    "recording_id": str(recording_id),
                    "sync_offset_s": offset,
                    "trim_start_s": trim_start,
                    "duration_s": duration_s,
                },
            )
        except Exception as exc:
            logger.warning(
                "audio_mux_error_video_only",
                extra={"recording_id": str(recording_id), "error": str(exc)},
            )
        finally:
            self._cleanup_audio_file(active)

    def _cleanup_audio_file(self, active: _ActiveRecording) -> None:
        """Delete the per-recording temp audio file, if any."""
        if active.audio_path is not None:
            with contextlib.suppress(OSError):
                active.audio_path.unlink(missing_ok=True)
            active.audio_path = None

    async def capture_photo(
        self,
        *,
        session_id: UUID,
        stream: str,
    ) -> CapturedPhoto:
        """Capture one still from the live visitor stream for photo-booth review."""
        recording_id = uuid7()
        out_path = self._settings.ssd_data_root / "photo-review" / f"photo_booth_{recording_id}.jpg"
        out_path.parent.mkdir(parents=True, exist_ok=True)
        proc = await asyncio.create_subprocess_exec(
            "ffmpeg",
            "-y",
            "-rtsp_transport",
            "tcp",
            "-i",
            self._settings.mediamtx_rtsp_url(stream),
            "-frames:v",
            "1",
            str(out_path),
            stdout=asyncio.subprocess.DEVNULL,
            stderr=asyncio.subprocess.PIPE,
        )
        _, stderr = await proc.communicate()
        if proc.returncode != 0:
            with contextlib.suppress(OSError):
                out_path.unlink(missing_ok=True)
            msg = stderr.decode(errors="replace")[:500]
            raise RuntimeError(f"ffmpeg still capture failed: {msg}")
        sha256 = _sha256_file(out_path)
        return CapturedPhoto(
            recording_id=recording_id,
            session_id=session_id,
            path=str(out_path.relative_to(self._settings.ssd_data_root)),
            size_bytes=out_path.stat().st_size,
            sha256=sha256,
            captured_monotonic_ms=time.monotonic_ns() // 1_000_000,
        )

    async def snapshot(self) -> bytes | None:
        """Grab a single current JPEG frame from the live visitor RTSP stream.

        Used by ``GET /snapshot`` (door-visiond's HardwareBackend polls it for
        face frames). ffmpeg is a **read-only consumer** of the existing RTSP
        stream, so this does not disturb the rpicam-vid publisher or MediaMTX's
        segment recording. The frame is written to ``pipe:1`` and returned as
        bytes — no file touches the SSD.

        Best-effort: the grab is bounded by ``snapshot_timeout_s`` and returns
        ``None`` on any failure (ffmpeg error, timeout, or a not-yet-live
        stream) so the caller can fall back to a placeholder. The door/face
        path must never block for long or 500 on a missing frame.
        """
        stream = self._settings.visitor_cam_stream
        rtsp_url = self._settings.mediamtx_rtsp_url(stream)
        timeout_s = self._settings.snapshot_timeout_s
        try:
            proc = await asyncio.create_subprocess_exec(
                "ffmpeg",
                "-nostdin",
                "-nostats",
                "-loglevel",
                "error",
                "-rtsp_transport",
                "tcp",
                "-i",
                rtsp_url,
                "-frames:v",
                "1",
                "-q:v",
                str(self._settings.snapshot_jpeg_quality),
                # image2 muxer refuses to write a single image to a pipe without
                # -update (it wants a %d filename pattern); -update 1 makes it emit
                # the one JPEG to stdout. Verified on-device.
                "-update",
                "1",
                "-f",
                "image2",
                "pipe:1",
                stdin=asyncio.subprocess.DEVNULL,
                stdout=asyncio.subprocess.PIPE,
                stderr=asyncio.subprocess.PIPE,
            )
        except Exception as exc:
            logger.warning("snapshot_spawn_failed", extra={"error": str(exc)})
            return None

        try:
            stdout, stderr = await asyncio.wait_for(proc.communicate(), timeout=timeout_s)
        except TimeoutError:
            with contextlib.suppress(ProcessLookupError):
                proc.kill()
            with contextlib.suppress(Exception):
                await proc.wait()
            logger.warning("snapshot_timeout", extra={"timeout_s": timeout_s})
            return None

        if proc.returncode != 0 or not stdout:
            message = stderr.decode(errors="replace")[:300]
            logger.warning(
                "snapshot_ffmpeg_failed",
                extra={"returncode": proc.returncode, "error": message},
            )
            return None
        return stdout

    def storage_status(self) -> MediaStorageStatus:
        """Delegate to OS disk stats for the SSD mount."""
        root = self._settings.ssd_data_root
        try:
            stat = shutil.disk_usage(root)
            free_bytes = stat.free
        except OSError:
            free_bytes = 0
        recording_allowed = free_bytes > self._settings.min_free_bytes
        return MediaStorageStatus(
            free_bytes=free_bytes,
            queue_depth=0,  # filled in by RecordingService from DB
            oldest_unsynced_s=0,
            recording_allowed=recording_allowed,
        )

    def stream_info(self) -> list[StreamInfo]:
        """Best-effort synchronous stream info (uses cached state)."""
        # We run an async version via health_check; here we return a cached view
        return [
            StreamInfo(
                name=self._settings.visitor_cam_stream,
                whep_url=(f"http://127.0.0.1:8889/{self._settings.visitor_cam_stream}/whep"),
                stream_up=False,  # updated by async path
                webrtc_clients=0,
            )
        ]

    async def stream_info_async(self) -> list[StreamInfo]:
        """Query MediaMTX API for current stream status."""
        stream = self._settings.visitor_cam_stream
        try:
            resp = await self._http.get(f"/v3/paths/get/{stream}")
            if resp.status_code == 200:
                data = resp.json()
                readers = len(data.get("readers", []))
                return [
                    StreamInfo(
                        name=stream,
                        whep_url=f"http://127.0.0.1:8889/{stream}/whep",
                        stream_up=True,
                        webrtc_clients=readers,
                    )
                ]
        except Exception as exc:
            logger.warning("stream_info_query_failed", extra={"error": str(exc)})
        return [
            StreamInfo(
                name=stream,
                whep_url=f"http://127.0.0.1:8889/{stream}/whep",
                stream_up=False,
                webrtc_clients=0,
            )
        ]

    async def health_check(self) -> bool:
        """Return True if MediaMTX API is reachable and the stream is live."""
        try:
            resp = await self._http.get(
                f"/v3/paths/get/{self._settings.visitor_cam_stream}",
            )
            return resp.status_code == 200
        except Exception:
            return False


