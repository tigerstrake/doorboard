def _segment_start_epoch(path: Path) -> float:
    """Parse a MediaMTX segment's start time (epoch seconds) from its path.

    MediaMTX writes segments at recordPath
    ``{segments_root}/{stream}/%Y/%m/%d/%H/%M/%S-%f`` (see
    ``_MEDIAMTX_CONFIG_TEMPLATE``), e.g.
    ``.../visitor/2026/07/18/14/30/45-500000.mp4``. ``%f`` is 6-digit
    microseconds (MediaMTX computes it as ``Start.Nanosecond()/1000``), and the
    timestamp is in the server's local time (``time.Local``; the template does
    not use ``%z``). So the year/month/day/hour/minute come from the parent
    directories and second + microseconds from the filename, reconstructed as a
    local-time ``datetime`` whose ``.timestamp()`` inverts MediaMTX's encoding.

    Raises ``ValueError``/``IndexError`` if the path does not match this layout
    (the caller falls back to the file mtime).
    """
    second_str, sep, micro_str = path.stem.partition("-")
    if not sep:
        raise ValueError(f"segment filename missing '%S-%f' separator: {path.name}")
    micros = int(micro_str)  # %f is zero-padded microseconds → int() is exact
    year, month, day, hour, minute = (int(part) for part in path.parent.parts[-5:])
    started = datetime(year, month, day, hour, minute, int(second_str), micros)
    return started.timestamp()


def _find_segments(
    seg_dir: Path,
    *,
    started_epoch: float,
    ended_epoch: float,
) -> list[Path]:
    """Return segment files whose modification time overlaps the wall-clock window."""
    if not seg_dir.exists():
        return []
    segments: list[tuple[float, Path]] = []
    for path in seg_dir.rglob("*.mp4"):
        try:
            modified = path.stat().st_mtime
        except OSError:
            continue
        if started_epoch <= modified <= ended_epoch:
            segments.append((modified, path))
    return [path for _, path in sorted(segments)]


def _prune_segments(root: Path, *, older_than_epoch: float) -> int:
    deleted = 0
    if not root.exists():
        return deleted
    for path in root.rglob("*.mp4"):
        try:
            if path.stat().st_mtime >= older_than_epoch:
                continue
            path.unlink()
            deleted += 1
        except FileNotFoundError:
            continue
        except OSError as exc:
            logger.warning("segment_delete_failed", extra={"path": str(path), "error": str(exc)})
    for directory in sorted((p for p in root.rglob("*") if p.is_dir()), reverse=True):
        with contextlib.suppress(OSError):
            directory.rmdir()
    return deleted


async def _concat_segments(segments: list[Path], out_path: Path) -> None:
    """Concatenate MP4 segments into a single output file using ffmpeg."""
    # Build ffmpeg concat list
    concat_list = out_path.parent / f".concat_{out_path.stem}.txt"
    concat_list.write_text(
        "\n".join(f"file '{seg}'" for seg in segments),
        encoding="utf-8",
    )
    try:
        proc = await asyncio.create_subprocess_exec(
            "ffmpeg",
            "-y",
            "-f",
            "concat",
            "-safe",
            "0",
            "-i",
            str(concat_list),
            "-c",
            "copy",
            str(out_path),
            stdout=asyncio.subprocess.DEVNULL,
            stderr=asyncio.subprocess.PIPE,
        )
        _, stderr = await proc.communicate()
        if proc.returncode != 0:
            message = stderr.decode(errors="replace")[:500]
            raise RuntimeError(f"ffmpeg concat failed ({proc.returncode}): {message}")
    finally:
        with contextlib.suppress(OSError):
            concat_list.unlink()


def _sha256_file(path: Path) -> str:
    h = hashlib.sha256()
    with path.open("rb") as fh:
        for chunk in iter(lambda: fh.read(65536), b""):
            h.update(chunk)
    return h.hexdigest()
